package pl.pp;

public class Kalkulator {
    public static void main(String[] args) {
        // Tworzenie zmiennej całkowitej x i przypisanie jej wartości 10
        int x = 10;

        // Obliczenie dwukrotności liczby x
        int dwukrotnoscX = x * 2;

        // Obliczenie wartości x^2 (x do kwadratu)
        int kwadratX = x * x;

        // Wyświetlenie wszystkich wartości na konsoli
        System.out.println("x = " + x);
        System.out.println("Dwukrotność x = " + dwukrotnoscX);
        System.out.println("x do kwadratu = " + kwadratX);
    }
}
